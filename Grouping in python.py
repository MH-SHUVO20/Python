print(numbers[2:8])#print(numbers[2:])
print(numbers[:5])#print number from start to 5
print(numbers[:])#print all numbers
print(numbers[::2])#print alternate numbers
print(numbers[1::2])#print alternate numbers starting from index 1
print(numbers[::-1])#print numbers in reverse order
print(numbers[8:2:-1])#print numbers from index 8 to 2
print(numbers[5:2:-1])#print numbers from index 5 to 2
print(numbers[5:2])#print empty list because step is +ve
print(numbers[-1:-6:-1])#print last 5 numbers in reverse order
print(numbers[-6:-1])#print last 5 numbers in normal order
print(numbers[-1:])#print last number
print(numbers[:-6])#print all numbers except last 5 numbers
print(numbers[-5:])#print last 5 numbers
print(numbers[2:-3])#print numbers from index 2 to -3
print(numbers[-3:2:-1])#print numbers from index -3 to 2 in reverse order
#print even numbers
print(numbers[1::2])
